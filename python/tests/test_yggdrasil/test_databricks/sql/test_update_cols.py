"""
Tests for the ``update_cols`` parameter of the SQL engine write path.

``update_cols`` (``mode=auto`` + ``match_by``) lets callers restrict the
columns that are mutated during a MERGE upsert.  The other write modes
(append, overwrite, truncate) ignore it.

Structure
---------
Unit tests (no live workspace)
    All IO is mocked so the SQL generated by ``arrow_insert_into`` can be
    inspected without any Databricks connection.

Integration tests (``requires_databricks``)
    :class:`TestUpdateColsIntegration` creates a real Delta table, exercises
    all three cases (default / explicit subset / empty), and verifies the
    resulting row data via ``to_arrow_table()``.
"""

from __future__ import annotations

import contextlib
from unittest.mock import MagicMock, patch

import pyarrow as pa
import pytest

from yggdrasil.databricks.client import DatabricksClient
from yggdrasil.databricks.sql.engine import SQLEngine
from yggdrasil.databricks.sql.table import Table

from ..conftest import DatabricksCase, requires_databricks

# ── shared fixtures & helpers ─────────────────────────────────────────────────

SCHEMA = pa.schema([
    pa.field("id",    pa.int64()),
    pa.field("name",  pa.string()),
    pa.field("value", pa.float64()),
])

SAMPLE = pa.table({
    "id":    pa.array([1, 2, 3], type=pa.int64()),
    "name":  ["a", "b", "c"],
    "value": pa.array([1.0, 2.0, 3.0], type=pa.float64()),
})

_LOCATION = "`main`.`sales`.`test_update_cols`"


@pytest.fixture()
def mock_client():
    return MagicMock(spec=DatabricksClient)


@pytest.fixture()
def mock_table():
    """Pre-configured Table mock backed by SCHEMA."""
    tbl = MagicMock(spec=Table)
    tbl.arrow_schema = SCHEMA
    tbl.full_name.return_value = _LOCATION
    tbl.full_name.side_effect = lambda safe=None: _LOCATION
    tbl.catalog_name = "main"
    tbl.schema_name = "sales"
    tbl.table_name = "test_update_cols"
    tbl.ensure_created.return_value = tbl
    return tbl


@contextlib.contextmanager
def _capture_arrow_sqls(engine: SQLEngine, table: MagicMock):
    """
    Patch all heavy IO inside ``arrow_insert_into`` and collect every SQL
    string that would be passed to ``engine.execute()``.

    ``SQLEngine`` is a frozen dataclass, so we patch at the *class* level.
    """
    fake_path = MagicMock()
    fake_path.__str__ = lambda _: (
        "/Volumes/main/sales/tmp/.sql/main/sales/test_update_cols/"
        "tmp-1774938955-1774942555-5ba89ace.parquet"
    )
    fake_path.write_bytes = MagicMock()
    fake_path.remove = MagicMock()
    fake_path.parent = MagicMock()
    fake_path.parent.mkdir = MagicMock()

    fake_staging = MagicMock()
    fake_staging.path = fake_path
    fake_staging.register_shutdown_cleanup = MagicMock()
    fake_staging.cleanup = MagicMock()

    mock_mio = MagicMock()
    mock_mio.buffer.memoryview.return_value = b""

    mock_bio_instance = MagicMock()
    mock_bio_instance.__enter__ = MagicMock(return_value=MagicMock())
    mock_bio_instance.__exit__ = MagicMock(return_value=False)

    captured: list[str] = []

    # Class-level patches work on frozen dataclasses; instance-level do not.
    def _fake_execute(sql, **_kw):
        captured.append(sql.strip())
        return MagicMock()

    with (
        patch("yggdrasil.databricks.sql.engine.StagingPath.for_table", return_value=fake_staging),
        patch("yggdrasil.databricks.sql.engine.BytesIO", return_value=mock_bio_instance),
        patch("yggdrasil.databricks.sql.engine.MediaIO") as mock_MediaIO,
        patch("yggdrasil.databricks.sql.engine.Job"),
        patch.object(SQLEngine, "execute", side_effect=_fake_execute),
    ):
        mock_MediaIO.make.return_value = mock_mio
        yield captured


def _run(engine, table, *, update_cols, mode="auto", match_by=None):
    """Call arrow_insert_into and return captured SQL statements."""
    with _capture_arrow_sqls(engine, table) as sqls:
        engine.arrow_insert_into(
            data=SAMPLE,
            table=table,
            match_by=match_by or ["id"],
            update_cols=update_cols,
            mode=mode,
        )
    return sqls


# ===========================================================================
# Unit — arrow_insert_into SQL generation
# ===========================================================================


class TestUpdateColsArrowSQL:
    """Unit tests — verify the MERGE SQL produced by arrow_insert_into."""

    @pytest.fixture(autouse=True)
    def _engine(self, mock_client, mock_table):
        self.engine = SQLEngine(client=mock_client, catalog_name="main", schema_name="sales")
        self.table = mock_table

    def test_default_updates_all_non_key_columns(self):
        """update_cols=None → all non-key columns appear in UPDATE SET."""
        sqls = _run(self.engine, self.table, update_cols=None)
        assert len(sqls) == 1
        merge = sqls[0]
        assert "WHEN MATCHED THEN UPDATE SET" in merge
        assert "`name`" in merge
        assert "`value`" in merge
        # key column must NOT appear in UPDATE SET
        assert "T.`id`" not in merge.split("WHEN MATCHED")[1]

    def test_explicit_subset_updates_only_those_columns(self):
        """update_cols=['value'] → only value in UPDATE SET, name absent."""
        sqls = _run(self.engine, self.table, update_cols=["value"])
        merge = sqls[0]
        assert "WHEN MATCHED THEN UPDATE SET" in merge
        assert "T.`value` = S.`value`" in merge
        # name must NOT appear in the UPDATE SET clause
        update_part = merge.split("WHEN MATCHED THEN UPDATE SET")[1].split("WHEN NOT MATCHED")[0]
        assert "`name`" not in update_part

    def test_empty_list_omits_update_clause(self):
        """update_cols=[] → no WHEN MATCHED UPDATE, still inserts new rows."""
        sqls = _run(self.engine, self.table, update_cols=[])
        merge = sqls[0]
        assert "WHEN MATCHED THEN UPDATE SET" not in merge
        assert "WHEN NOT MATCHED THEN INSERT" in merge

    def test_update_cols_not_applied_to_append_mode(self):
        """APPEND mode ignores update_cols; MERGE is insert-only."""
        sqls = _run(self.engine, self.table, update_cols=["value"], mode="append")
        merge = sqls[0]
        assert "WHEN MATCHED" not in merge
        assert "WHEN NOT MATCHED THEN INSERT" in merge

    def test_update_cols_not_applied_to_truncate_mode(self):
        """TRUNCATE mode: DELETE + INSERT, no MERGE UPDATE clause."""
        sqls = _run(self.engine, self.table, update_cols=["value"], mode="truncate")
        assert len(sqls) == 2
        assert sqls[0].startswith("DELETE FROM")
        assert sqls[1].startswith("INSERT INTO")
        assert "WHEN MATCHED" not in " ".join(sqls)

    def test_update_cols_insert_row_always_present(self):
        """The WHEN NOT MATCHED INSERT clause is always present regardless of update_cols."""
        for uc in [None, ["value"], []]:
            sqls = _run(self.engine, self.table, update_cols=uc)
            assert "WHEN NOT MATCHED THEN INSERT" in sqls[0], f"failed for update_cols={uc!r}"

    def test_merge_uses_null_safe_equality_on_key(self):
        """Keys in ON condition should use <=> (null-safe)."""
        sqls = _run(self.engine, self.table, update_cols=None)
        assert "T.`id` <=> S.`id`" in sqls[0]


