class TestUpdateColsPropagation:
    """Verify that insert_into forwards update_cols to the underlying path."""

    @pytest.fixture(autouse=True)
    def _setup(self, mock_client):
        self.engine = SQLEngine(client=mock_client)

    def test_arrow_path_receives_update_cols(self, mock_table):
        with (
            patch.object(SQLEngine, "arrow_insert_into") as mock_arrow,
            patch("yggdrasil.databricks.sql.engine.PyEnv") as mock_pyenv,
        ):
            mock_pyenv.spark_session.return_value = None
            self.engine.insert_into(
                data=SAMPLE,
                table=mock_table,
                match_by=["id"],
                update_cols=["value"],
                mode="auto",
            )
        mock_arrow.assert_called_once()
        _, kwargs = mock_arrow.call_args
        assert kwargs.get("update_cols") == ["value"]

    def test_spark_path_receives_update_cols(self, mock_table):
        mock_spark = MagicMock()
        with patch.object(SQLEngine, "spark_insert_into") as mock_spark_fn:
            self.engine.insert_into(
                data=SAMPLE,
                table=mock_table,
                match_by=["id"],
                update_cols=["name"],
                mode="auto",
                spark_session=mock_spark,
            )
        mock_spark_fn.assert_called_once()
        _, kwargs = mock_spark_fn.call_args
        assert kwargs.get("update_cols") == ["name"]


# ===========================================================================
# Integration — requires live Databricks workspace
# ===========================================================================

pytestmark_integration = [requires_databricks, pytest.mark.integration]

_TABLE = "test_engine_update_cols"

_INITIAL = pa.table({
    "id":    pa.array([1, 2, 3], type=pa.int64()),
    "name":  ["Alice", "Bob", "Charlie"],
    "value": pa.array([100.0, 200.0, 300.0], type=pa.float64()),
})

_UPSERT = pa.table({
    "id":    pa.array([1, 4], type=pa.int64()),   # row 1 exists, row 4 is new
    "name":  ["Alice-UPDATED", "Dave"],
    "value": pa.array([999.0, 400.0], type=pa.float64()),
})


class TestUpdateColsIntegration(DatabricksCase):
    """Integration tests that hit a real Delta table in the test catalog."""

    pytestmark = pytestmark_integration

    @classmethod
    def setUpClass(cls) -> None:
        super().setUpClass()
        cls.engine = cls.workspace.sql(catalog_name="trading", schema_name="unittest")
        # Drop from a prior failed run if needed
        cls.engine.drop_table(table_name=_TABLE, raise_error=False)
        # Create fresh table
        cls.engine.insert_into(_INITIAL, table_name=_TABLE, mode="overwrite")

    @classmethod
    def tearDownClass(cls) -> None:
        cls.engine.drop_table(table_name=_TABLE, raise_error=False)
        super().tearDownClass()

    def _read(self) -> pa.Table:
        result = self.engine.execute(
            f"SELECT id, name, value FROM {self.engine.tables.table(table_name=_TABLE).full_name(safe=True)}"
            f" ORDER BY id"
        )
        return result.to_arrow_table()

    def _reset(self):
        """Restore the table to its initial state."""
        self.engine.insert_into(_INITIAL, table_name=_TABLE, mode="overwrite")

    # ── default behaviour (update all non-key columns) ───────────────────────

    def test_default_updates_all_non_key_columns(self):
        self._reset()
        self.engine.insert_into(
            _UPSERT,
            table_name=_TABLE,
            mode="auto",
            match_by=["id"],
            # update_cols=None  → default
        )
        rows = {r["id"].as_py(): r for r in self._read().to_pylist()}

        # row 1: both name and value should be updated
        self.assertEqual(rows[1]["name"], "Alice-UPDATED")
        self.assertEqual(rows[1]["value"], 999.0)
        # row 2: untouched
        self.assertEqual(rows[2]["name"], "Bob")
        self.assertEqual(rows[2]["value"], 200.0)
        # row 4: inserted
        self.assertIn(4, rows)
        self.assertEqual(rows[4]["name"], "Dave")

    # ── explicit subset: only value updated, name unchanged ──────────────────

    def test_explicit_update_cols_restricts_update(self):
        self._reset()
        self.engine.insert_into(
            _UPSERT,
            table_name=_TABLE,
            mode="auto",
            match_by=["id"],
            update_cols=["value"],   # ← only update the value column
        )
        rows = {r["id"].as_py(): r for r in self._read().to_pylist()}

        # row 1: value updated, but name must stay "Alice" (not "Alice-UPDATED")
        self.assertEqual(rows[1]["name"], "Alice")
        self.assertEqual(rows[1]["value"], 999.0)
        # row 4: fully inserted (new row carries all columns)
        self.assertEqual(rows[4]["name"], "Dave")
        self.assertEqual(rows[4]["value"], 400.0)

    # ── empty list: matched rows are not updated at all ──────────────────────

    def test_empty_update_cols_skips_update_for_matched_rows(self):
        self._reset()
        self.engine.insert_into(
            _UPSERT,
            table_name=_TABLE,
            mode="auto",
            match_by=["id"],
            update_cols=[],   # ← no columns to update
        )
        rows = {r["id"].as_py(): r for r in self._read().to_pylist()}

        # row 1: original values must be untouched
        self.assertEqual(rows[1]["name"], "Alice")
        self.assertEqual(rows[1]["value"], 100.0)
        # row 4: new row is inserted regardless
        self.assertIn(4, rows)

    # ── update_cols ignored in non-upsert modes ──────────────────────────────

    def test_update_cols_ignored_in_append_mode(self):
        """In APPEND mode the MERGE never fires an UPDATE; update_cols is irrelevant."""
        self._reset()
        # Attempt to insert a row whose id already exists: it should be skipped
        self.engine.insert_into(
            pa.table({"id": [1], "name": ["GHOST"], "value": [0.0]}),
            table_name=_TABLE,
            mode="append",
            match_by=["id"],
            update_cols=["value"],   # should have no effect in append mode
        )
        rows = {r["id"].as_py(): r for r in self._read().to_pylist()}
        self.assertEqual(rows[1]["name"], "Alice")   # unchanged
        self.assertEqual(rows[1]["value"], 100.0)    # unchanged

    def test_update_cols_ignored_in_truncate_mode(self):
        """TRUNCATE + match_by does DELETE + INSERT, not MERGE; update_cols is irrelevant."""
        self._reset()
        batch = pa.table({"id": [1], "name": ["NewAlice"], "value": [777.0]})
        self.engine.insert_into(
            batch,
            table_name=_TABLE,
            mode="truncate",
            match_by=["id"],
            update_cols=["value"],   # should have no effect in truncate mode
        )
        rows = {r["id"].as_py(): r for r in self._read().to_pylist()}
        # Row 1 was deleted then re-inserted from batch (full row)
        self.assertEqual(rows[1]["name"], "NewAlice")
        self.assertEqual(rows[1]["value"], 777.0)
        # Rows 2 and 3 were NOT in the batch → should still exist (only matching keys deleted)
        self.assertIn(2, rows)
        self.assertIn(3, rows)

