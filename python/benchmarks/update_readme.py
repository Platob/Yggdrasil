"""Update the README ``Performance`` section from live benchmark results.

What this does
--------------

Runs a curated, representative slice of the benchmark suite (the
shapes pinned in :data:`_HIGHLIGHTS`), parses the per-line output,
and rewrites the block between
``<!-- BENCH_RESULTS_START -->`` and ``<!-- BENCH_RESULTS_END -->``
in the repo-root ``README.md`` with a fresh markdown table.

Intended to run in CI on release. Idempotent: re-running with no
benchmark changes only updates the numbers — labels, markers, and
surrounding copy are preserved.

Usage::

    PYTHONPATH=src python benchmarks/update_readme.py
    PYTHONPATH=src python benchmarks/update_readme.py --repeat 5 --dry-run
"""
from __future__ import annotations

import argparse
import re
import subprocess
import sys
from pathlib import Path
from typing import Iterable


REPO_ROOT = Path(__file__).resolve().parents[2]
BENCH_DIR = Path(__file__).resolve().parent
README = REPO_ROOT / "README.md"

START_MARKER = "<!-- BENCH_RESULTS_START -->"
END_MARKER = "<!-- BENCH_RESULTS_END -->"

#: ``(benchmark_stem, [(label_prefix, friendly_label)])`` — the curated
#: set surfaced on README.  Scoped to whole-table casts because that's
#: the shape readers care about when sizing the cost of moving frames
#: through yggdrasil; per-column timings live in
#: ``bench_cast_data.py`` directly.
_HIGHLIGHTS: list[tuple[str, list[tuple[str, str]]]] = [
    ("bench_cast_data", [
        ("arrow: cast_arrow_tabular convenient rows=10000",
         "Arrow → Arrow, convenient types (int/decimal/string/timestamp/bool/date), 10k rows"),
        ("arrow: cast_arrow_tabular nested rows=10000",
         "Arrow → Arrow, nested types (list / map / struct), 10k rows"),
        ("polars: cast_polars_tabular convenient rows=10000",
         "Polars → Polars, convenient types, 10k rows"),
        ("polars: cast_polars_tabular nested rows=10000",
         "Polars → Polars, nested types, 10k rows"),
        ("pandas: cast_pandas_tabular convenient rows=10000",
         "pandas → pandas, convenient types, 10k rows"),
    ]),
]


_BEST_RE = re.compile(r"best=\s*([0-9.]+)\s*(ns|us|ms|s)")


def _run_bench(name: str, *, repeat: int, extra: Iterable[str] = ()) -> dict[str, str]:
    """Run a single ``bench_*`` and return ``{label_prefix: formatted_time}``."""
    script = BENCH_DIR / f"{name}.py"
    if not script.is_file():
        return {}
    proc = subprocess.run(
        [sys.executable, str(script), "--repeat", str(repeat), *extra],
        cwd=BENCH_DIR.parent,
        check=True,
        capture_output=True,
        text=True,
    )
    out: dict[str, str] = {}
    for line in proc.stdout.splitlines():
        # Skip header / banner lines.
        if line.startswith("#") or "best=" not in line:
            continue
        # Lines have the shape:
        #   "label words ...    best=12.34 us  median=...  mean=..."
        # The label runs up to the first ``best=`` token (with the
        # trailing whitespace stripped).
        m = _BEST_RE.search(line)
        if m is None:
            continue
        label = line[: m.start()].rstrip()
        out[label] = _format(float(m.group(1)), m.group(2))
    return out


def _format(value: float, unit: str) -> str:
    """Render ``best`` in the most readable unit."""
    # Promote everything into seconds, then pick the nicest representation.
    seconds = value * {"ns": 1e-9, "us": 1e-6, "ms": 1e-3, "s": 1.0}[unit]
    if seconds < 1e-6:
        return f"~{seconds * 1e9:.0f} ns"
    if seconds < 1e-3:
        return f"~{seconds * 1e6:.1f} µs"
    if seconds < 1.0:
        return f"~{seconds * 1e3:.1f} ms"
    return f"~{seconds:.2f} s"


def _build_table(repeat: int) -> str:
    rows: list[str] = ["| Operation | Per call (best) |", "|---|---|"]
    for bench_name, entries in _HIGHLIGHTS:
        results = _run_bench(bench_name, repeat=repeat)
        for label_prefix, friendly in entries:
            value = None
            # Tolerate trailing-space / column-padding differences by
            # accepting any label that starts with ``label_prefix``.
            for label, formatted in results.items():
                if label.startswith(label_prefix):
                    value = formatted
                    break
            rows.append(f"| {friendly} | {value or 'n/a'} |")
    return "\n".join(rows)


def _splice_readme(table: str, *, readme: Path) -> str:
    text = readme.read_text()
    if START_MARKER not in text or END_MARKER not in text:
        raise SystemExit(
            f"README is missing {START_MARKER} / {END_MARKER} markers; "
            "add them around the benchmark block before running this script."
        )

    block = (
        f"{START_MARKER}\n"
        "<!-- Regenerated by python/benchmarks/update_readme.py — do not edit by hand. -->\n\n"
        "Whole-table cast latency on representative analytics shapes — the "
        "**convenient** schema mixes int / decimal / string / timestamp / bool / "
        "date, and **nested** stresses list / map / struct round-trips. Numbers "
        "are wall-clock per call, 10k rows, taken from `bench_cast_data.py`.\n\n"
        f"{table}\n\n"
        "Run the full sweep locally:\n\n"
        "```bash\n"
        "cd python\n"
        "python benchmarks/run_all.py --repeat 5\n"
        "```\n\n"
        "Per-column kernel timings (Arrow / Polars / pandas, MATCH / WIDEN / "
        "NARROW), schema-merge, equality, and `CastOptions` overhead all live "
        "in [`python/benchmarks/`](python/benchmarks/) and are picked up "
        "automatically by `run_all.py`.\n\n"
        f"{END_MARKER}"
    )
    pattern = re.compile(
        re.escape(START_MARKER) + r".*?" + re.escape(END_MARKER),
        re.DOTALL,
    )
    return pattern.sub(block, text)


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--repeat", type=int, default=5,
                    help="Outer repeat count per benchmark (median across).")
    ap.add_argument("--dry-run", action="store_true",
                    help="Print the regenerated README instead of writing it.")
    args = ap.parse_args()

    table = _build_table(args.repeat)
    new_text = _splice_readme(table, readme=README)

    if args.dry_run:
        sys.stdout.write(new_text)
        return 0

    README.write_text(new_text)
    print(f"# updated {README}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
